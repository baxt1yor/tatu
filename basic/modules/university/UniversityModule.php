<?php

namespace app\modules\university;

use app\models\User;
use yii\web\ForbiddenHttpException;

/**
 * university module definition class
 */
class UniversityModule extends \yii\base\Module
{
    public $layout = 'main';
    public static $access_roles = [User::ROLE_UNIVERSITY, User::ROLE_ADMIN];
    /**
     * @inheritdoc
     */
    public $controllerNamespace = 'app\modules\university\controllers';

    /**
     * @inheritdoc
     */
    public function init()
    {
        parent::init();

        // custom initialization code goes here
    }

    public function beforeAction($action)
    {
        if (!\Yii::$app->user->isGuest) {
            $role = \Yii::$app->user->identity->getRole();
            if (!in_array($role, static::$access_roles))
                throw new ForbiddenHttpException("Siz bu sahifaga kirish huquqiga ega emassiz");
        } else {
            throw new ForbiddenHttpException("Siz bu sahifaga kirish huquqiga ega emassiz");
        }
        return parent::beforeAction($action); // TODO: Change the autogenerated stub
    }
}
